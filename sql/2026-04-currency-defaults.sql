-- Централізовані дефолти валют на tenant-рівні.
-- =========================================================================
-- Owner налаштовує у owner-panel → Налаштування → Валюти. Cargo-CRM,
-- Passenger-CRM і сканер читають при старті і використовують як fallback
-- при створенні нового ліда / відкритті forms без збереженого значення.
--
-- Формат jsonb:
--   {
--     "cargo": {
--       "payment":  "EUR",   -- сума оплати посилки
--       "deposit":  "EUR",   -- завдаток
--       "np":       "UAH",   -- Нова Пошта
--       "tips":     "EUR"    -- чайові
--     },
--     "passenger": {
--       "ticket":   "EUR",   -- квиток
--       "deposit":  "EUR",   -- завдаток
--       "tips":     "EUR"    -- чайові
--     }
--   }
--
-- Пустий jsonb → коди використовують hardcoded fallback (EUR / UAH).
-- =========================================================================

ALTER TABLE public.system_settings
  ADD COLUMN IF NOT EXISTS currency_defaults jsonb NOT NULL DEFAULT '{}'::jsonb;
